    private void prepareView() {
        try {
            List<domain.Person> people = Communication.getInstance().getAllPeople();
            PeopleTableModel ptm = new PeopleTableModel(people);
            getTblPeople().setModel(ptm);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "ERROR DETAILS", JOptionPane.ERROR_MESSAGE);
        }
    }

