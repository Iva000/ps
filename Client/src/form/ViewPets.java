    private void prepareView() {
        try {
            List<domain.Pet> pets = Communication.getInstance().getAllPets();
            PetTableModel ptm = new PetTableModel(pets);
            getTblPets().setModel(ptm);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "ERROR DETAILS", JOptionPane.ERROR_MESSAGE);
        }
    }

